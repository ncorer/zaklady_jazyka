
public class MathDemo {

	public static void main(String[] args) {
		
		System.out.println(Math.PI);

		System.out.println(Math.pow(2, 4)); // prve cislo na druhe 2 na stvrtu
		
		System.out.println(Math.sqrt(100)); //druha odmocnina
		
		System.out.println(Math.random()); // nahodne cislo od nula po jedna
		
		int nahoda = (int) (Math.random() * 10); //nahodne cislo od jedna po 9
		
		System.out.println(nahoda);
	}

}
